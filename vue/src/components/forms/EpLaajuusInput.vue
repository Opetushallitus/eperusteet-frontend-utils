<template>
  <div v-if="!isEditing">
    <div v-if="model">
      {{ model }}
      <slot>
        {{ $t('osaamispiste') }}
      </slot>
    </div>
    <div v-else>
      -
    </div>
  </div>
  <div
    v-else
    class="d-flex flex-column"
  >
    <div class="d-flex align-items-center">
      <div class="flex-grow-1">
        <ep-input
          v-model="model"
          type="number"
          min="0"
          max="999"
          :is-editing="isEditing"
          :validation="validation"
        />
      </div>
      <div class="ml-2">
        <slot>
          {{ $t('osaamispiste') }}
        </slot>
      </div>
    </div>
  </div>
</template>

<script setup lang="ts">
import { computed, ref, watch } from 'vue';
import EpInput from '@shared/components/forms/EpInput.vue';
import EpErrorWrapper from '../forms/EpErrorWrapper.vue';
import { useVuelidate } from '@vuelidate/core';

const props = defineProps({
  modelValue: {
    type: Number,
    required: true,
  },
  isEditing: {
    type: Boolean,
    default: false,
  },
  validation: {
    type: Object,
    default: null,
  },
});

const emit = defineEmits(['update:modelValue']);

// Use vuelidate for validation
const v$ = useVuelidate();

// Initialize the model with the value from props
const model = ref(props.modelValue || 0);

// Watch for changes in the value prop
watch(() => props.modelValue, (newValue) => {
  model.value = newValue;
}, { immediate: true });

// Watch for changes to the model
watch(() => model.value, (newValue) => {
  // Constrain the value between 0 and 999
  model.value = Math.max(Math.min(999, Number(newValue)), 0);
  // Emit the update event with the new value
  emit('update:modelValue', model.value);
}, { immediate: true });
</script>

